#리스트 요소 제거
#1. 인덱스 제거
#1.1 del연산자
#1.2 pop()함수

#1.1 del
a = [1,2,3,4,5,6,7]
print(a) #[1, 2, 3, 4, 5, 6, 7]
del a[1]
print(a) #[1, 3, 4, 5, 6, 7]

#1.2 pop()
b = [1,2,3,4,5,6,7]
b.pop(1)
print(b) #[1, 3, 4, 5, 6, 7]


#2. 값으로 제거
#2.1 remove()함수

a = [100, 200, 300, 400, 500]
a.remove(100)
print(a)
#[200, 300, 400, 500]

a = [1,1,1,1,1]
a.remove(1)
print(a)
#[1, 1, 1, 1]


a = [1,2,3,4,5,6,7]
a.clear()
print(a) #[]


